package com.example.mad_assignment_1.databases.restaurants;

public class Restaurant {
    String id;
    String name;
    int image;

    public Restaurant(String id, String name, int image) {
        this.id = id;
        this.name = name;
        this.image = image;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getImage() {
        return image;
    }
}